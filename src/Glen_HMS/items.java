    public void table() {
        try {
            String sqlQuery = "select i.item_id, i.item_name, i.item_units, i.item_description, ic.item_category_name, "
                    + "i.item_reorder_level from item i, item_category ic where i.item_category = ic.item_category_id and i.item_availability = 'available' ORDER BY i.item_id ASC";

            Statement st = Database.newConnection().createStatement();
            ResultSet rs = st.executeQuery(sqlQuery);

            DefaultTableModel tbl = (DefaultTableModel) item_Tbl.getModel();
            tbl.setRowCount(0);

            while (rs.next()) {
                Vector vec = new Vector();
                vec.add(rs.getString("item_id"));
                vec.add(rs.getString("item_name"));
                vec.add(rs.getString("item_units"));
                vec.add(rs.getString("item_description"));
                vec.add(rs.getString("item_category_name"));
                vec.add(rs.getString("item_reorder_level"));
                tbl.addRow(vec);
            }

        } catch (Exception ex) {
            Logger.getLogger(stock_menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

