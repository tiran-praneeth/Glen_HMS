    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed

        try {
            String name = s_item_name.getText();
            String id = s_item_id.getText();
            String category = (String) search_cate.getSelectedItem();

            String sqlQuery = "select i.item_id, i.item_name, i.item_units, i.item_description, ic.item_category_name, "
                    + "i.item_reorder_level from item i, item_category ic where i.item_category = ic.item_category_id and"
                    + " i.item_availability = 'available' AND i.item_id LIKE'%" + id + "%' AND i.item_name LIKE'%" + name
                    + "%' AND ic.item_category_name LIKE'%" + category + "%' ORDER BY i.item_id ASC";

            Statement st = Database.newConnection().createStatement();
            ResultSet rs = st.executeQuery(sqlQuery);

            DefaultTableModel tbl = (DefaultTableModel) item_Tbl.getModel();
            tbl.setRowCount(0);

            while (rs.next()) {
                Vector vec = new Vector();
                vec.add(rs.getString("item_id"));
                vec.add(rs.getString("item_name"));
                vec.add(rs.getString("item_units"));
                vec.add(rs.getString("item_description"));
                vec.add(rs.getString("item_category_name"));
                vec.add(rs.getString("item_reorder_level"));
                tbl.addRow(vec);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Save Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_search_btnActionPerformed

