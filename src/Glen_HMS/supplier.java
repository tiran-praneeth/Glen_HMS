    private void search_butActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_butActionPerformed

        try {

            String name = supName.getText();
            String id = supId.getText();

            String sql = "select supplier_id, supplier_name, supplier_contact, supplier_address, supplier_email, "
                    + "supplier_description, DATE_FORMAT(supplier_reg_date, '%Y-%m-%d %H:%i') as 'Register_Date' "
                    + "from supplier where supplier_availability = 'available'"
                    + " AND supplier_id LIKE'%" + id + "%' AND supplier_name LIKE'%" + name + "%'";

            Statement st = Database.newConnection().createStatement();
            ResultSet rs = st.executeQuery(sql);

            // System.out.println(sql);
            DefaultTableModel tbl = (DefaultTableModel) Supplier_Tbl.getModel();
            tbl.setRowCount(0);

            while (rs.next()) {
                Vector vec = new Vector();
                vec.add(rs.getString("supplier_id"));
                vec.add(rs.getString("supplier_name"));
                vec.add(rs.getString("supplier_contact"));
                vec.add(rs.getString("supplier_address"));
                vec.add(rs.getString("supplier_email"));
                vec.add(rs.getString("supplier_description"));
                vec.add(rs.getString("Register_Date"));
                tbl.addRow(vec);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Save Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_search_butActionPerformed

