    public void table() {
        try {
            String sqlQuery = "select supplier_id, supplier_name, supplier_contact, supplier_address,"
                    + " supplier_email, supplier_description, DATE_FORMAT(supplier_reg_date, '%Y-%m-%d %H:%i') as 'Register_Date' from"
                    + " supplier where supplier_availability = 'available'";

            Statement st = Database.newConnection().createStatement();
            ResultSet rs = st.executeQuery(sqlQuery);

            DefaultTableModel tbl = (DefaultTableModel) Supplier_Tbl.getModel();
            tbl.setRowCount(0);

            while (rs.next()) {
                Vector vec = new Vector();
                vec.add(rs.getString("supplier_id"));
                vec.add(rs.getString("supplier_name"));
                vec.add(rs.getString("supplier_contact"));
                vec.add(rs.getString("supplier_address"));
                vec.add(rs.getString("supplier_email"));
                vec.add(rs.getString("supplier_description"));
                vec.add(rs.getString("Register_Date"));
                tbl.addRow(vec);
            }

        } catch (Exception ex) {
            Logger.getLogger(stock_menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

