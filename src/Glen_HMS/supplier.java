    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed

        String id = sup_id.getText();

        if (id != null && !"".equals(id)) {

            int x = JOptionPane.showConfirmDialog(this, "Are you sure you want to Update record?", "Confirm Message!", 0, JOptionPane.WARNING_MESSAGE);
            if (x == 0) {

                try {

                    String ID = sup_id.getText();
                    String name = sup_name.getText();
                    String address = sup_address.getText();
                    String des = description.getText();
                    String email = sup_email.getText();
                    String TP = mobile.getText();
                    String Date = date1.getText();

                    Statement st = Database.newConnection().createStatement();
                    st.executeUpdate("UPDATE supplier SET supplier_name='" + name + "', supplier_address='" + address + "', supplier_contact='" + TP
                            + "', supplier_email='" + email + "', supplier_description='" + des + "', supplier_reg_date='" + Date + "' WHERE supplier_id=" + ID);
                    System.out.println();
                    this.table();
                    JOptionPane.showMessageDialog(null, "Saved Successfuly", "Save", JOptionPane.INFORMATION_MESSAGE);

                } catch (Exception ex) {

                    JOptionPane.showMessageDialog(null, ex, "Save Error",
                            JOptionPane.ERROR_MESSAGE);
                }

                table();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please Select Supplier !",
                    "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_update_btnActionPerformed

